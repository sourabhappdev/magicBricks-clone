class PropertyModel {
  final String city;
  final String locality;
  final String address;
  final String propertyForBuySell;
  final String category;
  final String subCategory;
  final double carpetArea;
  final double superArea;
  final int numberBHK;
  final int numberWashroom;
  final int numberFloor;
  final String description;
  final String name;
  final String location;
  final double price;
  final String imageUrl;

  PropertyModel({
    required this.city,
    required this.locality,
    required this.address,
    required this.propertyForBuySell,
    required this.category,
    required this.subCategory,
    required this.carpetArea,
    required this.superArea,
    required this.numberBHK,
    required this.numberWashroom,
    required this.numberFloor,
    required this.description,
    required this.name,
    required this.location,
    required this.price,
    required this.imageUrl,
  });
}
